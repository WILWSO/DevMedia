//EXEMPLO 1 - REPLACE - SUBSTITUI CARACTERES NA STRING
//A função replace() é usada para substituir uma parte de uma string por outra string. A função não altera a string original, mas retorna uma nova string com a parte substituída. A função replace() é útil quando você deseja alterar uma parte de uma string, como quando você deseja corrigir um erro de digitação ou quando você deseja formatar uma string de uma maneira diferente.

let texto = "O Brasil é um país tropical"; //string
let texto_substituido = texto.replace("tropical", "maravilhoso"); //substitui a palavra "tropical" por "maravilhoso"
console.log(texto_substituido); //exibe a string com a parte substituída
