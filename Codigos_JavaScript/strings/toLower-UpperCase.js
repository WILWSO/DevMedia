//EXEMPLO 1 - TO LOWER CASE - CONVERTE A STRING PARA MINÚSCULAS
//A função toLowerCase() é usada para converter uma string para minúsculas. A função não altera a string original, mas retorna uma nova string com todos os caracteres convertidos para minúsculas.
//A função toLowerCase() é útil quando você deseja comparar duas strings sem se preocupar com a diferença entre maiúsculas e minúsculas. Por exemplo, se você deseja verificar se uma string é igual a outra, independentemente de como elas foram escritas (com letras maiúsculas ou minúsculas).

let programador_1 = "Wilton"; //string
let programador_2 = "wilton"; //string

console.log(programador_1.toUpperCase());
console.log(programador_2.toLowerCase());

